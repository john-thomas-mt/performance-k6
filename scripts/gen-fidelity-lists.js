/* Walk a NeoLoad VU tree and emit the tier-2 (UI chrome) and tier-3 (static) request lists a fidelity
   port fires behind -e FIDELITY=ui|full. Spine endpoints (scripted as correlated wrappers) are excluded
   so they are not double-fired. Requests are grouped by NeoLoad step so the flow can fire each slice in
   the matching group, keeping the extra load in-flight around the spine writes.
   Usage: node scripts/gen-fidelity-lists.js "<path to VU tree>" <chrome-out.ts> <static-out.ts> */
const fs = require('fs');
const path = require('path');

const [vuRoot, chromeOut, staticOut] = process.argv.slice(2);
if (!vuRoot || !chromeOut || !staticOut) {
  console.error('usage: node scripts/gen-fidelity-lists.js <vu-tree> <chrome-out.ts> <static-out.ts>');
  process.exit(1);
}
const ROOT = path.join(vuRoot, 'actions-container');

// spine endpoints scripted as correlated wrappers — never emit as chrome
const SPINE = [
  '/api/USIDataGridServer/CreateNewRowsWithDefaultValues',
  '/api/GenericDetailServer/Save2',
  '/api/USIDataGridServer/Save2',
  '/api/GenericDetailServer/GetInitialData2',
  '/api/GenericServer/ApplicationUnloading',
  '/api/WindowServer/GetWindowInfo',
  '/api/GenericServer/SignIn',
];
const STATIC_EXT = /\.(js|css|html|svg|png|ico|woff2?|map|jpg|jpeg|gif)(\?|$)/i;

const stepDirs = fs
  .readdirSync(ROOT)
  .filter((d) => /^@t\d+/.test(d) && fs.statSync(path.join(ROOT, d)).isDirectory())
  .sort();

const chrome = {};
const stat = {};

for (const step of stepDirs) {
  const stepNo = (step.match(/_(\d+)_/) || [])[1];
  if (!stepNo) continue;
  const dir = path.join(ROOT, step);
  for (const f of fs.readdirSync(dir).filter((x) => x.endsWith('.xml'))) {
    const xml = fs.readFileSync(path.join(dir, f), 'utf8');
    const method = (xml.match(/method="([^"]+)"/) || [])[1] || 'GET';
    const rawPath = (xml.match(/path="([^"]+)"/) || [])[1] || '';
    if (!rawPath) continue;
    const url = rawPath.replace(/^\/\$\{[^}]+\}/, '').replace(/^\/[^/]*(?=\/(api|app)\/)/, ''); // strip version segment
    if (SPINE.some((s) => url.startsWith(s))) continue;

    if (url.includes('/app/') || STATIC_EXT.test(url)) {
      (stat[stepNo] = stat[stepNo] || []).push({ path: url });
    } else if (url.includes('/api/')) {
      const m = xml.match(/<textPostContent>\s*<!\[CDATA\[([\s\S]*?)\]\]>/);
      const body = m ? m[1].replace(/\$\{[^}]+\}/g, '') : undefined;
      const req = { method, path: url };
      if (method !== 'GET' && body !== undefined) req.body = body;
      (chrome[stepNo] = chrome[stepNo] || []).push(req);
    }
  }
}

const banner =
  '/* Generated by scripts/gen-fidelity-lists.js from the NeoLoad T02 tree — do not hand-edit.\n   Regenerate after re-recording. Tier-2 (UI chrome) requests fired behind -e FIDELITY=ui|full. */';
fs.writeFileSync(
  chromeOut,
  `${banner}\nimport { ChromeRequest } from '../../utils/exports/types.exp.ts';\n\nexport const bookEventChrome: { [step: string]: ChromeRequest[] } = ${JSON.stringify(chrome, null, 2)};\n`,
);
fs.writeFileSync(
  staticOut,
  `/* Generated by scripts/gen-fidelity-lists.js — tier-3 static content fired behind -e FIDELITY=full. */\nimport { StaticRequest } from '../../utils/exports/types.exp.ts';\n\nexport const bookEventStatic: { [step: string]: StaticRequest[] } = ${JSON.stringify(stat, null, 2)};\n`,
);

const n = (o) => Object.values(o).reduce((a, v) => a + v.length, 0);
console.log(`chrome: ${n(chrome)} requests across ${Object.keys(chrome).length} steps -> ${chromeOut}`);
console.log(`static: ${n(stat)} requests across ${Object.keys(stat).length} steps -> ${staticOut}`);
