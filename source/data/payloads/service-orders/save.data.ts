/* Sets the requested quantity on a fetched catalog row (picked by CC716_SEQ) so it can be echoed into
   the order's items child table. */
export const pickCatalogItemRow = (catalog: TransportTable, seq: number, quantity: number): TransportRow => {
  const cols = catalog.TransportDataColumns.map((c) => c.ColumnName);
  const seqIdx = cols.indexOf('CC716_SEQ');
  const qtyIdx = cols.indexOf('cQUANTITY');
  const row = catalog.TransportDataRows.find((r) => String(r.Values[String(seqIdx)]) === String(seq));
  if (!row) {
    throw new Error(`pickCatalogItemRow: catalog item ${seq} not found`);
  }
  row.Values[String(qtyIdx)] = quantity;
  return row;
};

